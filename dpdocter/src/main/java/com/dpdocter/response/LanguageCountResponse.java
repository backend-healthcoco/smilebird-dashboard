package com.dpdocter.response;

public class LanguageCountResponse {
	private Integer english = 0;
	private Integer hindi = 0;
	private Integer marathi = 0;
	private Integer hinglish = 0;
	private Integer noValues = 0;

	public Integer getNoValues() {
		return noValues;
	}

	public void setNoValues(Integer noValues) {
		this.noValues = noValues;
	}
	public Integer getEnglish() {
		return english;
	}

	public void setEnglish(Integer english) {
		this.english = english;
	}

	public Integer getHindi() {
		return hindi;
	}

	public void setHindi(Integer hindi) {
		this.hindi = hindi;
	}

	public Integer getMarathi() {
		return marathi;
	}

	public void setMarathi(Integer marathi) {
		this.marathi = marathi;
	}

	public Integer getHinglish() {
		return hinglish;
	}

	public void setHinglish(Integer hinglish) {
		this.hinglish = hinglish;
	}

}
