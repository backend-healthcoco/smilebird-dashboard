public class HealthiensCustomer {
	private String vendor_customer_id;
	private String customer_status;
	private String ref_booking_id;
	private String ref_booking_status;
	
	private String name;
	private String mobileNumber;
	public String getVendor_customer_id() {
		return vendor_customer_id;
	}
	public void setVendor_customer_id(String vendor_customer_id) {
		this.vendor_customer_id = vendor_customer_id;
	}
	public String getCustomer_status() {
		return customer_status;
	}
	public void setCustomer_status(String customer_status) {
		this.customer_status = customer_status;
	}
	public String getRef_booking_id() {
		return ref_booking_id;
	}
	public void setRef_booking_id(String ref_booking_id) {
		this.ref_booking_id = ref_booking_id;
	}
	public String getRef_booking_status() {
		return ref_booking_status;
	}
	public void setRef_booking_status(String ref_booking_status) {
		this.ref_booking_status = ref_booking_status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	
	
	
}
