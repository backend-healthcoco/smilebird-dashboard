package com.dpdocter.beans;

public class YogaMultilingual {

	private String yogaTeacherName;
	private Language language;
	private String languageId;
	
	
	public String getYogaTeacherName() {
		return yogaTeacherName;
	}
	public void setYogaTeacherName(String yogaTeacherName) {
		this.yogaTeacherName = yogaTeacherName;
	}
	public Language getLanguage() {
		return language;
	}
	public void setLanguage(Language language) {
		this.language = language;
	}
	public String getLanguageId() {
		return languageId;
	}
	public void setLanguageId(String languageId) {
		this.languageId = languageId;
	}
	
	
}
