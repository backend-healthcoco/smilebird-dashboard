package com.dpdocter.beans;

public class DataCount {
	private Integer countOfListed = 0;
	private Integer countOfUnListed = 0;
	private Integer total = 0;

	public Integer getCountOfListed() {
		return countOfListed;
	}

	public void setCountOfListed(Integer countOfListed) {
		this.countOfListed = countOfListed;
	}

	public Integer getCountOfUnListed() {
		return countOfUnListed;
	}

	public void setCountOfUnListed(Integer countOfUnListed) {
		this.countOfUnListed = countOfUnListed;
	}

	public Integer getTotal() {
		return total;
	}

	public void setTotal(Integer total) {
		this.total = total;
	}

}
